var winner_banner = document.getElementById("winner");
var progress_chunk = document.getElementById('progress-chunk');

var score = 0;
var abc;
var a, b, c;

// Helper Functions 
function shuffle(a) {
  var j, x, i;
  for (i = a.length; i; i--) {
    j = Math.floor(Math.random() * i);
    x = a[i - 1];
    a[i - 1] = a[j];
    a[j] = x;
  }
}

function random_number() {
  return Math.floor((Math.random() * literary_devices.length));
}

// Main game called upon page load, correct answer selected, and skip button clicked
function play_game() {
  // Create 3 random numbers in range of how many literary _devices objects there are
  var correct_definition = random_number();
  var random_definition1 = random_number();
  var random_definition2 = random_number();

  // Ensure the random numbers generted are not the same, resulting in
  // showing the same definition twice.
  if (random_definition1 == correct_definition) {
    random_definition1 = random_number();
  }
  if (random_definition2 == correct_definition || random_definition2 == random_definition1) {
    random_definition2 = random_number();
  }

  // Shows a random term for the user to try and guess its definition
  device_to_match.innerHTML = literary_devices[correct_definition].name;

  // Get 2 random definitions, and one that matches the device_to_match definition
  a = literary_devices[correct_definition].example;
  b = literary_devices[random_definition1].example;
  c = literary_devices[random_definition2].example;

  // Store the 3 definitions into an array to be shuffled
  abc = [a, b, c];

  // Shuffle all 3 definitions so they are different each play
  shuffle(abc);

  // Loop through the array of shuffled definitions and inset them to the page
  for (var x = 0; x <= all_definitions.length - 1; x++) {
    all_definitions[x].innerHTML = abc[x];
  }

  if (score == 100) {
    winner();
  }

}

// Adding event listener to definitions
definition_wrapper.addEventListener("click", listen, false);

function listen(e) {
  e.stopPropagation();
  if (e.target !== e.currentTarget) {
    var clicked_definition = e.target.innerHTML;
    check_answer(clicked_definition);
  }
}

function check_answer(definition) {
  console.log(definition);
  if (definition == a) {
    correct_answer();
  } else if (definition == b || definition == c) {
    incorrect_answer();
  } else {
    console.log('something went wrong...');
  }
}

/* Error Screen stuff */
function show_screen(screen) {
  window.scrollTo(0, 0);
  screen.style.display = "block";
  setTimeout(function () {
    screen.style.display = "none";
  }, 500);
}

function incorrect_answer() {
  if (score > 0 && score < 100) {
    score -= 5;
  }
  decrease_score_bar();
  show_screen(incorrect_screen);
}

function correct_answer() {
  if (score >= 0 && score < 100) {
    score += 5;
  }
  increase_score_bar();
  show_screen(correct_screen);
  play_game();
}

function increase_score_bar() {
  setTimeout(function () {
    progress_chunk.style.width = score + "%";
  }, 500);
}

function decrease_score_bar() {
  setTimeout(function () {
    progress_chunk.style.width = score + "%";
  }, 500);
}

function winner() {
  winner_banner.style.top = "110px";
}

play_game();
document.getElementById('next').addEventListener("click", play_game);